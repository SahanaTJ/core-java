class Vessel {

  static String Name;
  static String Color;
  static String Price;
  
  static void setName(String nm){
  Name=nm;
  }
  static String getName(){
  return Name;
  }
  static void setColor(String clr){
  Color=clr;
  }
  static String getColor(){
  return Color;
  }
  static void setPrice(String prc){
  Price=prc;
  }
  static String getPrice(){
  return Price;
  }
 }