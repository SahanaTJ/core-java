class Toys {
   
   static String Name;
   static String Color;
   static String Price;
   
   static void setName(String nm){
     Name=nm;
   }
   static String getName(){
     return Name;
   }
   static void setColor(String cop){
     Color=cop;
   }
	static String getColor(){
	  return Color;
	}
    static void setPrice(String price){
	  Price=price;
	}
	static String getPrice(){
		return Price;
 }
}
	